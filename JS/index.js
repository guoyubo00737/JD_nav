(function search() {
    //搜索栏历史菜单
    var mean = document.querySelector('.main-search>input');
    var search = document.querySelector('.main-search .scope');
    var queue = [];
    var down = document.querySelector('.main-search-down');
    //对焦后显示后选 生成后选中a链接
    //给a链接绑定事件
    mean.addEvent('focus', function () {
        down.style.display = 'block';
        for (var i = 0; i < queue.length; i++) {
            down.innerHTML += '<a href=#>' + queue[i]; //安装了自动补结束标签插件
            down.innerHTML += '<br/>';
        }
        var realA = document.querySelectorAll('.main-search-down a');
        for (var i = 0; i < realA.length; i++) {
            realA[i].addEvent('mouseover', function () {
                mean.value = this.innerText;
            })
        }
    })
    //失焦后关闭后选和down元素内文字置空
    mean.addEvent('blur', function () {
        down.style.display = 'none';
        down.innerHTML = '';
    })

    //点击搜索后更新队列
    //队列长度为3
    search.addEvent('click', function () {
        if (mean.value !== '' && queue.indexOf(mean.value) === -1) {
            if (queue.length <= 2) {
                queue.push(mean.value);
            } else {
                queue.shift();
                queue.push(mean.value);
            }
        }
    })

    //快速选择上搜索栏
    for (var i = 0; i < document.querySelectorAll('.quick-search a').length; i++) {
        document.querySelectorAll('.quick-search a')[i].addEvent('click', function () {
            mean.value = this.innerText;
        })
    }
})();

